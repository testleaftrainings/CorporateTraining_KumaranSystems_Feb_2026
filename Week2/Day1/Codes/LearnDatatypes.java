package org.kumaransystems.week2.day1;

public class LearnDatatypes {
    public static void main(String[] args) {
        //byte
        byte age=22;   
        System.out.println(age);
        
        //short
        short ageOfPerson=130;
        System.out.println(ageOfPerson);

        //int
        int populationOfCity=1000000;
        System.out.println(populationOfCity);
        
        //long
        long amount=8925411170l;
        System.out.println(amount);

        //float and double
        float num1=1.123456789f;
        System.out.println(num1);
        double num2=1.123456789;
        System.out.println(num2);

        //char
        char logoOfCompany='T';
        System.out.println(logoOfCompany);
        
        //boolean
        boolean doYouKnowJava=false;
        System.out.println(doYouKnowJava);

        String nameOfCompany="Kumaran Systems";
        System.out.println(nameOfCompany);
    }
}
