package org.kumaransystems.week2.day1;

public class LearnForLoop {
    //print 1 to 10 numbers
    public static void main(String[] args) {
        for(int i=1;i<=10;i++){
            System.out.println(i);
        }
    System.out.println("----------------------");
    //print 10 to 1 numbers
         for(int i=10;i>=1;i--){
            System.out.println(i);
        }
    }

    
   
}
